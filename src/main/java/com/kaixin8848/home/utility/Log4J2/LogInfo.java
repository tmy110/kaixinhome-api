package com.kaixin8848.home.utility.Log4J2;

/**
 * 日志message对象
 */
public class LogInfo {
    private String moduleBig;
    private String moduleSmall;
    private String filter1;
    private String filter2;

    public String getModuleBig() {
        return moduleBig;
    }

    public void setModuleBig(String moduleBig) {
        this.moduleBig = moduleBig;
    }

    public String getModuleSmall() {
        return moduleSmall;
    }

    public void setModuleSmall(String moduleSmall) {
        this.moduleSmall = moduleSmall;
    }

    public String getFilter1() {
        return filter1;
    }

    public void setFilter1(String filter1) {
        this.filter1 = filter1;
    }

    public String getFilter2() {
        return filter2;
    }

    public void setFilter2(String filter2) {
        this.filter2 = filter2;
    }
}
